    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {   
       
        if ($request->hasFile('icon')) {
            $imagePath = $request->file('icon')->store('images');
            $iconPath = Storage::url($imagePath);
        } else {
            $defaultImagePath = 'images/producto-sin-imagen.png';
            $iconPath = Storage::url($defaultImagePath);
        }

        $category = new Category;
        $category -> name = $request -> name;
        $category -> detail = $request -> detail;
        $category->icon = $iconPath;
        $category -> category_parent_id	 = $request -> category_parent_id;
        $category->save();
        return response()->json(['message' => 'Category created successfully'], 201);
    }

